unsigned QASMMCCodeEmitter::getCCOpValue(const MCInst &MI, unsigned OpNo,
                           SmallVectorImpl<MCFixup> &Fixups,
                           const MCSubtargetInfo &STI) const {
  
  // TODO: Update the switch case and return value
  const MCOperand &MO = MI.getOperand(OpNo);
  assert(MO.isImm() && "Immediate operand expected");
  switch (MO.getImm()) {
  case QASMCC::NE: return 0;
  case QASMCC::EQ:  return 1;
  case QASMCC::LO: return 2;
  case QASMCC::HS: return 3;
  case QASMCC::NB:  return 4;
  case QASMCC::GE: return 5;
  case QASMCC::LT:  return 6;
  default:
    llvm_unreachable("Unknown condition code");
  }
}

