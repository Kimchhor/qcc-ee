#ifndef LLVM_LIB_TARGET_QASM_QASMSUBTARGET_H
#define LLVM_LIB_TARGET_QASM_QASMSUBTARGET_H

#include "QASMFrameLowering.h"
#include "QASMISelLowering.h"
#include "QASMInstrInfo.h"
#include "QASMRegisterInfo.h"
#include "llvm/CodeGen/MachineScheduler.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetFrameLowering.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "QASMGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class QASMSubtarget : public QASMGenSubtargetInfo {
  Triple TargetTriple;
  virtual void anchor();

  QASMInstrInfo InstrInfo;
  QASMTargetLowering TLInfo;
  // SelectionDAGTargetInfo TSInfo;
  QASMFrameLowering FrameLowering;

public:
  QASMSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
                const TargetMachine &TM);

  // Automatically generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const QASMInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const QASMRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const QASMTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  bool enableMachineScheduler() const override { return true; }

  void overrideSchedPolicy(MachineSchedPolicy &Policy,
                           unsigned NumRegionInstrs) const override {
      
    // Policy.OnlyBottomUp = true;
    Policy.OnlyTopDown = true;
  }

#if 0
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
#endif
};

} // end namespace llvm

#endif
